import { readFile, writeFile } from "node:fs/promises";

const publicKey = process.env.RELEASE_PUBLIC_KEY_FILE
  ? await readFile(process.env.RELEASE_PUBLIC_KEY_FILE, "utf8")
  : process.env.RELEASE_PUBLIC_KEY_PEM;
if (!publicKey?.includes("BEGIN PUBLIC KEY")) throw new Error("RELEASE_PUBLIC_KEY_PEM is required");
await writeFile(
  "apps/cli/src/release-key.ts",
  `// Generated by release CI.\nexport const RELEASE_PUBLIC_KEY_PEM = ${JSON.stringify(publicKey)};\n`
);
